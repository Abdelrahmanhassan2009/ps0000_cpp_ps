#include <iostream>
using namespace std;

int main()
{
   // prompting user for number 2/2
   int number;
   cout<<"please enter a number "<<endl;
   
   // checking if the number is odd or even 3/3
   cin >> number;
   if (number % 2 == 0)
   {
      cout << "even" << endl;
   }
   else
   {
      cout << "odd" <<endl;
   }
   
   return 0;
}
